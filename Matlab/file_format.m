Format of the raw ASCII file (option -r):

1: TOW, GPS Time of Week (seconds)

2: SVID

3: Signal type: 0=GPS_L1CA, 3=GPS_L2C, 4=GPS_L5,
                8=GLO_L1CA, 11=GLO_L2CA,
                17=GAL_L1BC, 20=GAL_E5a, 21=GAL_E5b, 22=GAL_AltBOC,
                24=GEO_L1CA,
                6=QZS_L1CA, 7=QZS_L2C, 26=QZS_L5,
                28=CMP_B1,
                29=CMP_B2

4: Carrier phase (cycles)

5: I correlation (dimensionless)

6: Q correlation (dimensionless)